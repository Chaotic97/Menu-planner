/**
 * Generate loading spinner HTML.
 * @param {string} [message='Loading...']
 * @returns {string}
 */
export function loadingHTML(message = 'Loading...') {
  return `<div class="loading"><div class="loading-spinner"></div><p>${message}</p></div>`;
}

/**
 * Generate empty state HTML with icon and optional CTA.
 * @param {object} opts
 * @param {string} opts.icon - key from EMPTY_ICONS (dishes, menus, tasks, etc.)
 * @param {string} opts.title - heading text
 * @param {string} [opts.message] - subtitle text
 * @param {string} [opts.actionLabel] - button text
 * @param {string} [opts.actionId] - button id for event binding
 * @returns {string}
 */
export function emptyStateHTML({ icon = 'generic', title, message = '', actionLabel = '', actionId = '' } = {}) {
  const svg = EMPTY_ICONS[icon] || EMPTY_ICONS.generic;
  let html = `<div class="empty-state"><div class="empty-state-icon">${svg}</div><h3 class="empty-state-title">${title}</h3>`;
  if (message) html += `<p class="empty-state-message">${message}</p>`;
  if (actionLabel && actionId) {
    html += `<button class="btn btn-primary" id="${actionId}">${actionLabel}</button>`;
  }
  html += '</div>';
  return html;
}
